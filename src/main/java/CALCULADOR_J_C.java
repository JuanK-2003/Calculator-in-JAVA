    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        this.Segundonumero=Float.parseFloat(this.Casilla.getText());
        switch(this.Operador)
        {
            case "+": this.Casilla.setText(Sin_Cero(this.Primernumero + this.Segundonumero));break;
            case "-": this.Casilla.setText(Sin_Cero(this.Primernumero - this.Segundonumero));break;
            case "*": this.Casilla.setText(Sin_Cero(this.Primernumero * this.Segundonumero));break;
            case "/": if(this.Segundonumero == 0)
            {
                this.Casilla.setText("        **E R R O R   M A T E M √Å T I C O** ");
            }
            else
            {
                this.Casilla.setText(Sin_Cero(this.Primernumero / this.Segundonumero));break;
            }      
        }
    }//GEN-LAST:event_jButton11ActionPerformed

