    public String Sin_Cero(float Resultado)
    {
        String Retorno="";
        
        Retorno = Float.toString(Resultado);
        if(Resultado %1 == 0)
        {
            Retorno = Retorno.substring(0, Retorno.length()-2);
        }
        return Retorno;
    }
            
